class DynamicSparseGate(nn.Module):
    def __init__(self, channels, reduction=16, init_tau=5.0, tau_decay=0.97, batches_per_epoch=189):
        super().__init__()
        self.channels = channels
        self.tau_decay = tau_decay
        self.batches_per_epoch = batches_per_epoch

        self.gate_conv = nn.Sequential(
            nn.Conv2d(channels, channels // reduction, 1),
            nn.Mish(),
            nn.Conv2d(channels // reduction, channels, 1)
        )

        self.register_buffer('tau', torch.tensor(float(init_tau)))
        self.register_buffer('min_tau', torch.tensor(0.5))
        self.register_buffer('batch_counter', torch.tensor(0))
        self.register_buffer('epoch_counter', torch.tensor(0))

        nn.init.normal_(self.gate_conv[-1].weight, std=0.01)
        nn.init.constant_(self.gate_conv[-1].bias, 0.0)

    def _auto_anneal(self):
        if self.training:
            self.batch_counter += 1

            if self.batch_counter >= self.batches_per_epoch:
                with torch.no_grad():
                    self.tau.data *= self.tau_decay
                    self.tau.data.clamp_(min=self.min_tau)
                    self.epoch_counter += 1
                    self.batch_counter.zero_()

    def forward(self, x):
        self._auto_anneal()

        B, C = x.shape[:2]
        z = x.mean(dim=[2, 3], keepdim=True)
        theta = self.gate_conv(z).view(B, C)

        if self.training:
            gumbel_noise = -torch.log(-torch.log(torch.rand_like(theta) + 1e-10))
            gate = (theta + gumbel_noise) / self.tau
        else:
            gate = theta / self.tau

        gate = torch.sigmoid(gate)
        if self.training:
            hard_gate = (gate > 0.5).float()
            gate = gate + (hard_gate - gate.detach())
        else:
            gate = (gate >= 0.5).float()
        # if self.batch_counter == self.batches_per_epoch-1:
        #     active_ratio = (gate > 0.5).float().mean().item()
        #     print(f"Epoch, Tau {self.tau:.3f}, Active Ratio: {active_ratio:.2%}")
        return x * gate.view(B, C, 1, 1)

class SWF(nn.Module):
    def __init__(self, dimension=1,):
        super(SWF, self).__init__()
        self.d = dimension
        self.w = nn.Parameter(torch.ones(2, dtype=torch.float32), requires_grad=True)
        self.v = nn.Parameter(torch.ones(1, dtype=torch.float32), requires_grad=True)
        self.epsilon = 0.0001

    def forward(self, x):
        w = self.w
        v = self.v
        weight = v[0] * w / (torch.sum(w, dim=0) + self.epsilon)
        x = [weight[0] * x[0], weight[1] * x[1]]
        t = torch.cat(x, self.d)
        return t
class CGLU(nn.Module):
    def __init__(self, in_features, hidden_features=None, out_features=None, act_layer=nn.GELU):
        super().__init__()
        out_features = out_features or in_features
        hidden_features = hidden_features or in_features
        hidden_features = int(2 * hidden_features / 3)
        self.fc1 = nn.Conv2d(in_features, hidden_features * 2, kernel_size=1)
        self.dwconv = nn.Conv2d(hidden_features, hidden_features, kernel_size=3, stride=1, padding=1, bias=True,
                                groups=hidden_features)
        self.act = act_layer()
        self.fc2 = nn.Conv2d(hidden_features, out_features, kernel_size=1)
    def forward(self, x):
        x, v = self.fc1(x).chunk(2, dim=1)
        x = self.act(self.dwconv(x) + x) * v
        x = self.fc2(x)
        return x

class LayerNorm2d(nn.Module):

    def __init__(self, normalized_shape, eps=1e-6, elementwise_affine=True):
        super().__init__()
        self.norm = nn.LayerNorm(normalized_shape, eps, elementwise_affine)

    def forward(self, x):
        x = rearrange(x, 'b c h w -> b h w c').contiguous()
        x = self.norm(x)
        x = rearrange(x, 'b h w c -> b c h w').contiguous()
        return x
class SSBlock(nn.Module):
    def __init__(self, in_channels, hidden_dim: int = 0, ssm_d_state=16, ffn_ratio=2, ssm_ratio=2.0, ssm_dt_rank="auto", ssm_act_layer=nn.Mish, ssm_conv=3, ssm_conv_bias=True,
                 ssm_drop_rate=0., ssm_init="v0", forward_type="v2", channel_first=False, dyt=False, glu=True):
        super().__init__()
        self.op = SS2D(
            d_model=hidden_dim,
            d_state=ssm_d_state,
            ssm_ratio=ssm_ratio,
            dt_rank=ssm_dt_rank,
            act_layer=ssm_act_layer,
            # ==========================
            d_conv=ssm_conv,
            conv_bias=ssm_conv_bias,
            # ==========================
            dropout=ssm_drop_rate,
            # bias=False,
            # ==========================
            # dt_min=0.001,
            # dt_max=0.1,
            # dt_init="random",
            # dt_scale="random",
            # dt_init_floor=1e-4,
            initialize=ssm_init,
            # ==========================
            forward_type=forward_type,
            channel_first=channel_first,
        )
        if glu:
            self.ffn = CGLU(
                in_features=in_channels, hidden_features=int(in_channels * ffn_ratio))
        else:
            self.ffn = nn.Sequential(nn.Conv2d(in_channels, int(in_channels * ffn_ratio)),
                                     nn.GELU(),
                                     nn.Conv2d(int(in_channels * ffn_ratio), in_channels))
        if dyt:
            pass
        else:
            self.norm1 = nn.LayerNorm(hidden_dim)
            self.norm2 = nn.LayerNorm(hidden_dim)

    def forward(self, x):

        x1 = rearrange(x, 'b c h w -> b h w c').contiguous()
        x2 = self.op(self.norm1(x1))
        x1 = rearrange(x2, 'b h w c -> b c h w').contiguous()
        x1 = x1 + x
        x2 = rearrange(x1, 'b c h w -> b h w c').contiguous()
        x2 = self.norm2(x2)
        x2 = rearrange(x2, 'b h w c -> b c h w').contiguous()
        x = x1 + self.ffn(x2)

        return x

class RMELAN(nn.Module):
    def __init__(self, in_channels, out_channels, n=1, e=2, ssm_state=12, ssm_ratio=2, ffn_ratio=4):
        super().__init__()
        self.e = e
        self.inc = in_channels
        self.outc = out_channels
        self.split_channels = int(out_channels * 0.25)
        self.conv_channels = int(self.split_channels)
        self.ss_channels = int(self.split_channels)
        self.num = 1 + 2 * n
        if self.e == 2:
            ssm_state = 1
            ssm_ratio = 2
            self.ss1 = nn.Sequential(*(
                SSBlock(in_channels=self.ss_channels, hidden_dim=self.ss_channels, ssm_d_state=ssm_state,
                        ffn_ratio=ffn_ratio, ssm_ratio=ssm_ratio) for _ in range(1)))
            self.lconv1 = nn.Sequential(*(Bottleneck(self.ss_channels, self.ss_channels) for _ in range(1)))
            self.num = 3 + 2 * n
        self.conv1 = Conv(in_channels, self.split_channels)
        self.lconv = nn.Sequential(*(Bottleneck(self.ss_channels, self.ss_channels) for _ in range(n)))
        self.ss = nn.Sequential(*(
        SSBlock(in_channels=self.ss_channels, hidden_dim=self.ss_channels, ssm_d_state=ssm_state, ffn_ratio=ffn_ratio,
                ssm_ratio=ssm_ratio) for _ in range(n)))
        self.trans = Conv(self.num * self.split_channels, self.outc)
        self.mix = DynamicSparseGate(self.num * self.split_channels)

    def forward(self, x):
        x1 = self.conv1(x)
        x2 = self.ss(x1)
        x3 = self.lconv(x1)
        if self.e == 2:
            x4 = self.ss1(x3)
            x5 = self.lconv1(x2)
            mix = torch.cat((x1, x2, x3, x4, x5), 1)
        else:
            mix = torch.cat((x1, x2, x3), 1)

        return self.trans(self.mix(mix)) + x if self.inc == self.outc else self.trans(self.mix(mix))


class DEPMD(nn.Module):
    def __init__(self, dim, out_dim):
        super().__init__()
        self.hidden = int(dim * 4)

        self.pw_linear = nn.Sequential(
            nn.Conv2d(self.hidden, out_dim, kernel_size=1, stride=1, padding=0),
            nn.BatchNorm2d(out_dim),
            nn.Mish()
        )
        self.se = se(out_dim)
        # self.se = CBAM(out_dim)
        self.alpha = nn.Parameter(torch.ones(1))
    def forward(self, x):
        y = torch.cat([
            x[..., ::2, ::2],
            x[..., 1::2, ::2],
            x[..., ::2, 1::2],
            x[..., 1::2, 1::2]
        ], dim=1)
        y = self.pw_linear(y)
        return y + self.se(y) * self.alpha

class mamba_init:
    @staticmethod
    def dt_init(dt_rank, d_inner, dt_scale=1.0, dt_init="random", dt_min=0.001, dt_max=0.1, dt_init_floor=1e-4):
        dt_proj = nn.Linear(dt_rank, d_inner, bias=True)

        # Initialize special dt projection to preserve variance at initialization
        dt_init_std = dt_rank ** -0.5 * dt_scale
        if dt_init == "constant":
            nn.init.constant_(dt_proj.weight, dt_init_std)
        elif dt_init == "random":
            nn.init.uniform_(dt_proj.weight, -dt_init_std, dt_init_std)
        else:
            raise NotImplementedError

        # Initialize dt bias so that F.softplus(dt_bias) is between dt_min and dt_max
        dt = torch.exp(
            torch.rand(d_inner) * (math.log(dt_max) - math.log(dt_min))
            + math.log(dt_min)
        ).clamp(min=dt_init_floor)
        # Inverse of softplus: https://github.com/pytorch/pytorch/issues/72759
        inv_dt = dt + torch.log(-torch.expm1(-dt))
        with torch.no_grad():
            dt_proj.bias.copy_(inv_dt)
        # Our initialization would set all Linear.bias to zero, need to mark this one as _no_reinit
        # dt_proj.bias._no_reinit = True

        return dt_proj

    @staticmethod
    def A_log_init(d_state, d_inner, copies=-1, device=None, merge=True):
        # S4D real initialization
        A = torch.arange(1, d_state + 1, dtype=torch.float32, device=device).view(1, -1).repeat(d_inner, 1).contiguous()
        A_log = torch.log(A)  # Keep A_log in fp32
        if copies > 0:
            A_log = A_log[None].repeat(copies, 1, 1).contiguous()
            if merge:
                A_log = A_log.flatten(0, 1)
        A_log = nn.Parameter(A_log)
        A_log._no_weight_decay = True
        return A_log

    @staticmethod
    def D_init(d_inner, copies=-1, device=None, merge=True):
        # D "skip" parameter
        D = torch.ones(d_inner, device=device)
        if copies > 0:
            D = D[None].repeat(copies, 1).contiguous()
            if merge:
                D = D.flatten(0, 1)
        D = nn.Parameter(D)  # Keep in fp32
        D._no_weight_decay = True
        return D

    @classmethod
    def init_dt_A_D(cls, d_state, dt_rank, d_inner, dt_scale, dt_init, dt_min, dt_max, dt_init_floor, k_group=4):
        # dt proj ============================
        dt_projs = [
            cls.dt_init(dt_rank, d_inner, dt_scale, dt_init, dt_min, dt_max, dt_init_floor)
            for _ in range(k_group)
        ]
        dt_projs_weight = nn.Parameter(torch.stack([t.weight for t in dt_projs], dim=0))  # (K, inner, rank)
        dt_projs_bias = nn.Parameter(torch.stack([t.bias for t in dt_projs], dim=0))  # (K, inner)
        del dt_projs

        # A, D =======================================
        A_logs = cls.A_log_init(d_state, d_inner, copies=k_group, merge=True)  # (K * D, N)
        Ds = cls.D_init(d_inner, copies=k_group, merge=True)  # (K * D)
        return A_logs, Ds, dt_projs_weight, dt_projs_bias


class SS2Dv0:
    def __initv0__(
            self,
            # basic dims ===========
            d_model=96,
            d_state=16,
            ssm_ratio=2.0,
            dt_rank="auto",
            # ======================
            dropout=0.0,
            # ======================
            seq=False,
            force_fp32=True,
            **kwargs,
    ):
        if "channel_first" in kwargs:
            assert not kwargs["channel_first"]
        act_layer = nn.SiLU
        dt_min = 0.001
        dt_max = 0.1
        dt_init = "random"
        dt_scale = 1.0
        dt_init_floor = 1e-4
        bias = False
        conv_bias = True
        d_conv = 3
        k_group = 4
        factory_kwargs = {"device": None, "dtype": None}
        super().__init__()
        d_inner = int(ssm_ratio * d_model)
        dt_rank = math.ceil(d_model / 16) if dt_rank == "auto" else dt_rank

        self.forward = self.forwardv0
        if seq:
            self.forward = partial(self.forwardv0, seq=True)
        if not force_fp32:
            self.forward = partial(self.forwardv0, force_fp32=False)

        # in proj ============================
        self.in_proj = nn.Linear(d_model, d_inner * 2, bias=bias)
        self.act: nn.Module = act_layer()
        self.conv2d = nn.Conv2d(
            in_channels=d_inner,
            out_channels=d_inner,
            groups=d_inner,
            bias=conv_bias,
            kernel_size=d_conv,
            padding=(d_conv - 1) // 2,
            **factory_kwargs,
        )

        # x proj ============================
        self.x_proj = [
            nn.Linear(d_inner, (dt_rank + d_state * 2), bias=False)
            for _ in range(k_group)
        ]
        self.x_proj_weight = nn.Parameter(torch.stack([t.weight for t in self.x_proj], dim=0))  # (K, N, inner)
        del self.x_proj

        # dt proj, A, D ============================
        self.A_logs, self.Ds, self.dt_projs_weight, self.dt_projs_bias = mamba_init.init_dt_A_D(
            d_state, dt_rank, d_inner, dt_scale, dt_init, dt_min, dt_max, dt_init_floor, k_group=4,
        )

        # out proj =======================================
        self.out_norm = nn.LayerNorm(d_inner)
        self.out_proj = nn.Linear(d_inner, d_model, bias=bias)
        self.dropout = nn.Dropout(dropout) if dropout > 0. else nn.Identity()

    def forwardv0(self, x: torch.Tensor, seq=False, force_fp32=True, **kwargs):
        x = self.in_proj(x)
        x, z = x.chunk(2, dim=-1)  # (b, h, w, d)
        z = self.act(z)
        x = x.permute(0, 3, 1, 2).contiguous()
        x = self.conv2d(x)  # (b, d, h, w)
        x = self.act(x)
        selective_scan = partial(selective_scan_fn, backend="mamba")

        B, D, H, W = x.shape
        D, N = self.A_logs.shape
        K, D, R = self.dt_projs_weight.shape
        L = H * W

        x_hwwh = torch.stack([x.view(B, -1, L), torch.transpose(x, dim0=2, dim1=3).contiguous().view(B, -1, L)],
                             dim=1).view(B, 2, -1, L)
        xs = torch.cat([x_hwwh, torch.flip(x_hwwh, dims=[-1])], dim=1)  # (b, k, d, l)

        x_dbl = torch.einsum("b k d l, k c d -> b k c l", xs, self.x_proj_weight)
        if hasattr(self, "x_proj_bias"):
            x_dbl = x_dbl + self.x_proj_bias.view(1, K, -1, 1)
        dts, Bs, Cs = torch.split(x_dbl, [R, N, N], dim=2)
        dts = torch.einsum("b k r l, k d r -> b k d l", dts, self.dt_projs_weight)

        xs = xs.view(B, -1, L)  # (b, k * d, l)
        dts = dts.contiguous().view(B, -1, L)  # (b, k * d, l)
        Bs = Bs.contiguous()  # (b, k, d_state, l)
        Cs = Cs.contiguous()  # (b, k, d_state, l)

        As = -self.A_logs.float().exp()  # (k * d, d_state)
        Ds = self.Ds.float()  # (k * d)
        dt_projs_bias = self.dt_projs_bias.float().view(-1)  # (k * d)

        # assert len(xs.shape) == 3 and len(dts.shape) == 3 and len(Bs.shape) == 4 and len(Cs.shape) == 4
        # assert len(As.shape) == 2 and len(Ds.shape) == 1 and len(dt_projs_bias.shape) == 1
        to_fp32 = lambda *args: (_a.to(torch.float32) for _a in args)

        if force_fp32:
            xs, dts, Bs, Cs = to_fp32(xs, dts, Bs, Cs)

        if seq:
            out_y = []
            for i in range(4):
                yi = selective_scan(
                    xs.view(B, K, -1, L)[:, i], dts.view(B, K, -1, L)[:, i],
                    As.view(K, -1, N)[i], Bs[:, i].unsqueeze(1), Cs[:, i].unsqueeze(1), Ds.view(K, -1)[i],
                    delta_bias=dt_projs_bias.view(K, -1)[i],
                    delta_softplus=True,
                ).view(B, -1, L)
                out_y.append(yi)
            out_y = torch.stack(out_y, dim=1)
        else:
            out_y = selective_scan(
                xs, dts,
                As, Bs, Cs, Ds,
                delta_bias=dt_projs_bias,
                delta_softplus=True,
            ).view(B, K, -1, L)
        assert out_y.dtype == torch.float

        inv_y = torch.flip(out_y[:, 2:4], dims=[-1]).view(B, 2, -1, L)
        wh_y = torch.transpose(out_y[:, 1].view(B, -1, W, H), dim0=2, dim1=3).contiguous().view(B, -1, L)
        invwh_y = torch.transpose(inv_y[:, 1].view(B, -1, W, H), dim0=2, dim1=3).contiguous().view(B, -1, L)
        y = out_y[:, 0] + inv_y[:, 0] + wh_y + invwh_y

        y = y.transpose(dim0=1, dim1=2).contiguous()  # (B, L, C)
        y = self.out_norm(y).view(B, H, W, -1)

        y = y * z
        out = self.dropout(self.out_proj(y))
        return out



